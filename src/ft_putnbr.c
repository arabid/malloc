#include "../includes/malloc.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}


void	ft_putnbr(size_t n)
{
	if (n > 9)
	{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
	}
	else
	{
		ft_putchar(n+'0');
	}
}